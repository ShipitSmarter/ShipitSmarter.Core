    CMK = 9,

    /// <summary>
    /// Foot
    /// </summary>
    FOT = 10,

    /// <summary>
    /// Square Feet
    /// </summary>
    FTK = 11,

    /// <summary>
    /// Gram
    /// </summary>
    GRM = 12,

    /// <summary>
    /// Inch
    /// </summary>
    INH = 13,

    /// <summary>
    /// Microgram
    /// </summary>
    MC = 14,

    /// <summary>
    /// Milligram
    /// </summary>
    MGM = 15,

    /// <summary>
    /// Milliliter
    /// </summary>
    MMT = 16,

    /// <summary>
    /// Square Meters
    /// </summary>
    MTK = 17,

    /// <summary>
    /// Yards
    /// </summary>
    YRD = 18,
}
